int main(void){
    Int N;cin>>N;
    Int A,B,C;cin>>A>>B>>C;

    int M=9999;
    Int ans=M+1;
    for(int i=0;i<=M;i++){
        for(int j=0;j+i<=M;j++){
            Int d=N-A*i-B*j;
            if(d<0)continue;
            if(d%C==0){
                ans=min(ans,d/C+i+j);
            }
        }
    }
    cout<<ans<<"\n";
}
int main(void){
    int r,s;cin>>r>>s;
    int cnt=0;
    for(int i=0;i<=r;i++){
        for(int j=i;j<=r;j++){
            int k=s-i-j;
            if(j<=k && k<=r){
                int c=(i==j)+(j==k);
                if(c==2)cnt++;
                else if(c==1)cnt+=3;
                else cnt+=6;
            }
        }
    }
    cout<<cnt<<"\n";
}
int n, m;
vector<vector<int>> v;
void dfs(int i, vector<int> _v){
    if(i > m)return;
    if(_v.size() == n){
        v.push_back(_v);
        return;
    }
    dfs(i+1, _v);
    _v.push_back(i+1);
    dfs(i+1, _v);
}
int main(void){
    cin >> n >> m;
    dfs(0, {});

    sort(v.begin(),v.end());
    for(auto r:v){
        for(auto c:r){
            cout<<c<<" ";
        }cout<<"\n";
    }
}